const contractInstance = new web3.eth.Contract(contractABI, contractAddress);

async function mintNFT(amount) {
  await contractInstance.methods
    .cost()
    .call()
    .then(function (_cost) {
      var mintPrice = $("#price").text() * 10 ** 18;
      // console.log("total amount is "+amount);
      // console.log("price is "+mintPrice);
      contractInstance.methods
        .mint(amount)
        .send({
          to: contractAddress, // Required except during contract publications.
          from: window.ethereum.selectedAddress, // must match user's active address.
          value: "" + amount * _cost,
        })
        .then(() => {
          contractInstance.methods
            .totalSupply()
            .call()
            .then(function (_supply) {
              $(".total-mints").text(_supply);
            });
        });
    });
}

var oneMintCost = 0;

function ConnectWallet() {
  const [errorMessage, setErrorMessage] = useState(null);
  const [connButtonText, setConnButtonText] = useState("Connect Wallet");
  const [walletConnected, setWalletConnected] = useState(false);
  const [account, setAccount] = useState(null);
  const [balance, setBalance] = useState(null);
  const [defaultAccount, setDefaultAccount] = useState(null);
  const [userBalance, setUserBalance] = useState(null);
  const [count, setCount] = useState(1);
  const [amount, setAmount] = useState(0);
  const [mintCost, setMintCost] = useState(0);
  const [modal, setModal] = useState(false);

  contractInstance.methods
    .cost()
    .call()
    .then(function (_cost) {
      setAmount(_cost / 10 ** 18);
      setMintCost(_cost / 10 ** 18);
    });

  useEffect(() => {
    contractInstance.methods
      .totalSupply()
      .call()
      .then(function (_supply) {
        $(".total-mints").text(_supply);
      });

    contractInstance.methods
      .cost()
      .call()
      .then(function (_cost) {
        oneMintCost = _cost / 10 ** 18;
        $("#price").text(oneMintCost);
      });
  }, []);

  useEffect(() => {
    if (window.ethereum) {
      window.ethereum.on("accountsChanged", accountsChanged);
      window.ethereum.on("chainChanged", chainChanged);
    }
  }, []);

  const connectHandler = async () => {
    if (window.ethereum) {
      try {
        const res = await window.ethereum.request({
          method: "eth_requestAccounts",
        });
        await accountsChanged(res[0]);
        setWalletConnected(true);
        setConnButtonText("Wallet Connected");
      } catch (err) {
        console.error(err);
        setErrorMessage("There was a problem connecting to MetaMask");
      }
    } else {
      alert("Please Install MetaMask");
    }
  };

  //tosty message
  useEffect(() => {
    if (walletConnected) {
      toast.success("Wallet Connected", {
        position: "bottom-right",
        autoClose: 5000,
        hideProgressBar: false,
        closeOnClick: true,
        pauseOnHover: true,
        draggable: true,
        progress: undefined,
        type: "success",
        theme: "dark",
      });
    }
  }, [walletConnected]);

  const accountsChanged = async (newAccount) => {
    setAccount(newAccount);
    try {
      const balance = await window.ethereum.request({
        method: "eth_getBalance",
        params: [newAccount.toString(), "latest"],
      });
      setBalance(ethers.utils.formatEther(balance));
    } catch (err) {
      console.error(err);
      setErrorMessage("There was a problem connecting to MetaMask");
    }
  };

  const chainChanged = () => {
    setErrorMessage(null);
    setAccount(null);
    setBalance(null);
  };

  //increment decmrent by 0.15
  const increment = () => {
    if (count < 10) {
      setCount(count + 1);
      // var cost = mintCost;
      // setAmount(totalCost + oneMintCost);
      $("#price").text(oneMintCost * (count + 1));
    }
  };

  const decrement = () => {
    if (count > 1) {
      setCount(count - 1);
      $("#price").text(oneMintCost * (count - 1));
    }
  };

  function _mintNFT() {
    // console.log("num is "+$(".nft-num").val());
    mintNFT($(".nft-num").val());
    //triiger  modal
    setModal(true);
  }

  return (
    // conectbtn

    <>
      <ToastContainer />
      <div id="" className="connect__pos">
        <div className="flex_32">
          {/* {connButtonText} */}
          {walletConnected ? (
            <>
              <Popup
                trigger={
                  <img
                    onClick={() => {
                      setModal(true);
                    }}
                    className="img_conect1"
                    src="/images/mint.png"
                    alt=""
                  />
                }
                modal
                nested
              >
                {(close) => (
                  <div className="modal ">
                    <button className="close" onClick={close}>
                      x
                    </button>

                    <h1 className=" mb-3 text_st">Mint NFT</h1>

                    <p className="price">
                      <span id="price">{oneMintCost}</span> ETH
                    </p>

                    <div className="content1 pad_con">
                      <div className="">
                        <input
                          type="number"
                          disabled={true}
                          className="input__style  nft-num"
                          placeholder="0"
                          readyonly
                          value={count}
                          onChange={(e) => setCount(e.target.value)}
                        />
                      </div>

                      <div className="div__btns">
                        <button className="btn_plus" onClick={increment}>
                          +
                        </button>
                        <button className="btn_minus" onClick={decrement}>
                          -
                        </button>
                      </div>

                      <button className="mint__btn" onClick={_mintNFT}>
                        Mint
                      </button>
                    </div>
                  </div>
                )}
              </Popup>
            </>
          ) : (
            <>
              <img
                onClick={connectHandler}
                className="img_conect1"
                src="/images/Connect.png"
                alt=""
              />
            </>
          )}
        </div>
      </div>
    </>
  );
}

export default ConnectWallet;
